    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        try{
            Connection mycon = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","admin","admin");
            Statement st = mycon.createStatement();
           ResultSet result1 = st.executeQuery("select id from section where name = '"+section+"'");
            while(result1.next()){
                
                section_id = (String)(result1.getString(1));
                
            }
            PreparedStatement prepareStatement = mycon.prepareStatement("INSERT INTO DELIVERABLE(ID, NAME, TOTALMARKS, DEADLINE, SECTION_ID, DESCRIPTION )" + " VALUES (?, ?, ?, ?, ?, ?)");
            prepareStatement.setString(1, deliverableID_tf.getText());
            prepareStatement.setString(2, name_tf.getText());
            prepareStatement.setString(3, totalMArks_tf.getText());
            prepareStatement.setString(4, deadline_tf.getText());
            prepareStatement.setString(5, section_id);
            prepareStatement.setString(6, jTextArea1.getText());
            
            prepareStatement.executeUpdate();
            JOptionPane.showMessageDialog(f, "Deliverable Added","Success", JOptionPane.PLAIN_MESSAGE);
          
           
        }
        catch (Exception e){
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(f, "Could'nt Add!!","Failure", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addbtnActionPerformed

