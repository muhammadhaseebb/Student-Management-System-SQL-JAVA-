    private void udateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_udateBtnActionPerformed
        // TODO add your handling code here:
        deliverable_id = (String) jComboBox1.getSelectedItem();

        try{
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","admin","admin");
            Statement st = con.createStatement();
            ResultSet result = st.executeQuery("select student_regno, obtained_marks from Marks where deliverable_id = '"+deliverable_id+"'");
            while(result.next()){

                DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                model.addRow(new Object[]
                    {result.getString(1),
                        result.getString(2),
                        
                    }
                );

            }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_udateBtnActionPerformed

    private void view() {                                         
        // TODO add your handling code here:
        try{
            Connection mycon = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","admin","admin");
            
            Statement st = mycon.createStatement();

           
            
            
            
             ResultSet result = st.executeQuery("select d.id\n" +
                                                "from deliverable d join section s\n" +
                                                "on s.id = d.section_id\n" +
                                                "where s.name = '"+section+"'");
            while(result.next()){
                
                jComboBox1.addItem(result.getString(1));
           
            }
           
        }
        catch (Exception e){
            System.out.println(e.toString());
        }
       
       
        
    } 
